import { existsSync, mkdirSync, readdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawn, spawnSync } from "node:child_process";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const devDir = join(root, ".dev");
const testWorkspaceDir = join(devDir, "workspace");
const testWorkspaceMarker = join(testWorkspaceDir, ".reasonix-dev-workspace");
const primaryTestFile = join(testWorkspaceDir, "reasonix-smoke.md");
const userDataDir = join(devDir, `vscode-user-data-${process.pid}`);
const extensionsDir = join(devDir, "vscode-extensions");
const userSettingsFile = join(userDataDir, "User", "settings.json");

mkdirSync(devDir, { recursive: true });
prepareTestWorkspace();
closeOldDevHosts();
cleanupOldUserDataDirs();
mkdirSync(userDataDir, { recursive: true });
mkdirSync(extensionsDir, { recursive: true });
mkdirSync(dirname(userSettingsFile), { recursive: true });
writeFileSync(
  userSettingsFile,
  JSON.stringify(
    {
      "security.workspace.trust.enabled": false,
      "reasonix.autoStart": false,
      "reasonix.uiLanguage": "zh-CN"
    },
    null,
    2
  ),
  "utf8"
);

run("npm", ["run", "build"]);

const codeBin = findCodeBinary();
const args = [
  "--new-window",
  `--extensionDevelopmentPath=${root}`,
  `--user-data-dir=${userDataDir}`,
  `--extensions-dir=${extensionsDir}`,
  "--disable-extension",
  "github.copilot",
  "--disable-extension",
  "github.copilot-chat",
  testWorkspaceDir,
  primaryTestFile
];

console.log(`Opening VS Code Extension Development Host with ${testWorkspaceDir}`);
const child = spawn(codeBin, args, {
  cwd: root,
  detached: true,
  stdio: "ignore",
  shell: process.platform === "win32"
});
child.unref();

function run(command, args) {
  const result = spawnSync(command, args, {
    cwd: root,
    stdio: "inherit",
    shell: process.platform === "win32"
  });
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

function findCodeBinary() {
  const candidates = [
    process.env.VSCODE_BIN,
    "code",
    "code-insiders",
    "/Applications/Visual Studio Code.app/Contents/Resources/app/bin/code",
    "/Applications/Visual Studio Code - Insiders.app/Contents/Resources/app/bin/code"
  ].filter(Boolean);

  for (const candidate of candidates) {
    const result = spawnSync(candidate, ["--version"], {
      cwd: root,
      encoding: "utf8",
      shell: process.platform === "win32"
    });
    if (result.status === 0) {
      return candidate;
    }
  }

  console.error("Could not find the VS Code CLI. Install the `code` command or set VSCODE_BIN.");
  process.exit(1);
}

function prepareTestWorkspace() {
  if (existsSync(testWorkspaceDir) && !existsSync(testWorkspaceMarker)) {
    console.error(`Refusing to overwrite existing dev workspace without marker: ${testWorkspaceDir}`);
    console.error("Move or remove that directory, or add the marker only if it is safe for dev:host to regenerate it.");
    process.exit(1);
  }

  rmSync(testWorkspaceDir, { recursive: true, force: true });
  mkdirSync(testWorkspaceDir, { recursive: true });
  writeFileSync(
    testWorkspaceMarker,
    "Generated by npm run dev:host. This directory is safe to delete.\n",
    "utf8"
  );
  writeFileSync(
    primaryTestFile,
    [
      "# Reasonix Dev Host",
      "",
      "This workspace is generated by `npm run dev:host` and is safe to delete.",
      "",
      "Manual smoke checks:",
      "",
      "- Open the Reasonix activity bar view.",
      "- Type `/` and confirm the command suggestion menu opens.",
      "- Type `@` and confirm file and folder suggestions open.",
      "- Send a short prompt after configuring the Reasonix CLI if needed.",
      "",
      "Useful sample context:",
      "",
      "```ts",
      "export function helloReasonix(name: string): string {",
      "  return `hello ${name}`;",
      "}",
      "```",
      ""
    ].join("\n"),
    "utf8"
  );
}

function cleanupOldUserDataDirs() {
  for (const entry of readdirSync(devDir, { withFileTypes: true })) {
    if (!entry.isDirectory() || !entry.name.startsWith("vscode-user-data")) {
      continue;
    }

    const target = join(devDir, entry.name);
    if (target !== userDataDir) {
      rmSync(target, { recursive: true, force: true });
    }
  }
}

function closeOldDevHosts() {
  if (process.platform === "win32") {
    return;
  }

  const pids = findDevHostPids();
  for (const pid of pids) {
    try {
      process.kill(pid, "SIGTERM");
    } catch {
      // Process may have already exited.
    }
  }

  const remainingAfterTerm = waitForDevHostsToExit(3500);
  if (remainingAfterTerm.length === 0) {
    return;
  }

  for (const pid of remainingAfterTerm) {
    try {
      process.kill(pid, "SIGKILL");
    } catch {
      // Process may have already exited.
    }
  }

  const remainingAfterKill = waitForDevHostsToExit(1200);
  if (remainingAfterKill.length > 0) {
    console.error(`Could not close old Extension Development Host processes: ${remainingAfterKill.join(", ")}`);
    process.exit(1);
  }
}

function findDevHostPids() {
  const result = spawnSync("pgrep", ["-f", "--", `--extensionDevelopmentPath=${root}`], {
    cwd: root,
    encoding: "utf8"
  });
  return (result.stdout ?? "")
    .split(/\s+/)
    .map((value) => Number(value))
    .filter((pid) => Number.isInteger(pid) && pid > 0 && pid !== process.pid);
}

function waitForDevHostsToExit(timeoutMs) {
  const started = Date.now();
  let remaining = findDevHostPids();
  while (remaining.length > 0 && Date.now() - started < timeoutMs) {
    Atomics.wait(new Int32Array(new SharedArrayBuffer(4)), 0, 0, 100);
    remaining = findDevHostPids();
  }
  return remaining;
}
